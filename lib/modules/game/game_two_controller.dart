class GameTwoController {

  bool isMyTurn;
  bool discard;
  bool showAnimationSnoop;
  bool showAnimationDiscard;

  GameTwoController({
    this.isMyTurn = false,
    this.discard = false,
    this.showAnimationSnoop = false,
    this.showAnimationDiscard = false,
  });

  void setIsMyTurn(){
    isMyTurn != isMyTurn;
  }

  void setDiscard(){
    discard != discard;
  }
  void setShowAnimationSnoop(){
    isMyTurn != isMyTurn;
  }
  void setShowAnimationDiscard(){
    showAnimationDiscard != showAnimationDiscard;
  }
}